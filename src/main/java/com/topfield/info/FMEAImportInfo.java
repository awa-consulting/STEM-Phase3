    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        int i = jComboBox1.getSelectedIndex();
        
        if (i ==0) {
            jLabel19.setText("<html>The file Exported from STEMS Software and Wish to upload again. <br/>"
                            + "This will upated the STEMS Fmea data from Spread sheet data </html>");
            jLabel18.setIcon(new javax.swing.ImageIcon(getClass().getResource("/coachspecs/images/FmeaImport.png")));
        }else if (i==1) {
            jLabel19.setText("<html>The file is not an Exported STEMS File  and Wish you upload to STEMS <br/>"
                            + "This will created new Fmea in STEMS Software and Calc File</html>"); 
            jLabel18.setIcon(new javax.swing.ImageIcon(getClass().getResource("/coachspecs/images/FmeaImportExt.png")));
        }
        
        
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    
