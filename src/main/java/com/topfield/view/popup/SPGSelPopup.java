    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
       Item item = (Item)jComboBox2.getSelectedItem();
        if (item != null) {
       SubProductGroup spg = spgDao.getSPGById(item.getId());
       
        for (SubProductComponents subProductComponents : spg.getSubProductComponentsCollection()) {
                ref = new JCheckBox(subProductComponents.getSpcDescription());
                ref.setActionCommand(""+subProductComponents.getSpcId());
                checkBoxPan.add(ref);
                checkBoxPan.revalidate();
                checkBoxPan.repaint();
                
        }
       }
    }//GEN-LAST:event_jComboBox2ItemStateChanged


