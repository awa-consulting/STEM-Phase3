    public void setInvisible(boolean flag){
       jComboBox2.setVisible(flag);
       jLabel2.setVisible(flag);
        setSPGComCheck(flag);
    }
    
    public List<SubProductGroup> getSpcList(){
        if (spcList== null) {
            spcList = spgDao.getSPGByMpg(mpgId);
        } 
    return spcList;
    }
    
    public void setSPGComCheck(boolean flag){
    
        
        if (flag) {
             jComboBox2.removeAllItems(); 
        } else {
             checkBoxPan.removeAll(); 
        }
        
        for (SubProductGroup spgObj : getSpcList()) {
            if (flag) {

               jComboBox2.addItem(new Item(spgObj.getSpgId(), spgObj.getSpgDescription()));
 
            } else {
                ref = new JCheckBox(spgObj.getSpgDescription());
                ref.setActionCommand(""+spgObj.getSpgId());
                checkBoxPan.add(ref);
            }
        }
    
         checkBoxPan.repaint();
    }

    
