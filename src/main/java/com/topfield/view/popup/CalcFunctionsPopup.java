    public void setSILTarget(int i){
    
        Double lowTar =0.0;
        Double highTar = 0.0;
        Double intial = 0.0;
        
        if (i>0) {
            if (jComboBox1.getSelectedIndex() ==0) {
            intial=1.0;
            }else{
                 intial = 0.0001;
            }

             highTar =  intial/(Math.pow(10, i));
             lowTar = highTar/10; 
        }
        
             
 
        jTextField5.setText(NumberConversion.scientificNotation(lowTar, 2));
        jTextField7.setText(NumberConversion.scientificNotation(highTar.toString(), 2));
   
    }

